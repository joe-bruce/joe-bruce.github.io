function checkCell(node) {
  const i = Number(node.dataset.i);
  const indices = [i];
  // not on left edge
  if (i % ROW_LENGTH) indices.push(i - 1);
  // not on right edge
  if (i % ROW_LENGTH < ROW_LENGTH - 1) indices.push(i + 1);
  // not on top edge
  if (i >= ROW_LENGTH) indices.push(i - ROW_LENGTH);
  // not on left edge and not on top edge
  if (i >= ROW_LENGTH && i % ROW_LENGTH) indices.push(i - ROW_LENGTH - 1);
  // not on right edge and not on top edge
  if (i >= ROW_LENGTH && i % ROW_LENGTH < ROW_LENGTH - 1)
    indices.push(i - ROW_LENGTH + 1);
  // not on bottom edge
  if (i < node.parentNode.children.length - ROW_LENGTH)
    indices.push(i + ROW_LENGTH);
  // not on left edge and not on bottom edge
  if (i < node.parentNode.children.length - ROW_LENGTH && i % ROW_LENGTH)
    indices.push(i + ROW_LENGTH - 1);
  // not on right edge and not on bottom edge
  if (
    i < node.parentNode.children.length - ROW_LENGTH &&
    i % ROW_LENGTH < ROW_LENGTH - 1
  )
    indices.push(i + ROW_LENGTH + 1);

  const counts = indices.reduce(
    function(counts, i) {
      const className = node.parentNode.children[i].className.split(" ")[0];
      counts[className] = (counts[className] || 0) + 1;
      return counts;
    },
    { filled: 0, empty: 0 }
  );

  return (
    String(Number(node.textContent)) !== node.textContent ||
    (counts.filled <= Number(node.textContent) &&
      counts.empty <= indices.length - Number(node.textContent))
  );
}

