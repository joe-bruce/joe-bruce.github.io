function toggleCell(event) {
  const node = event.target;
  if (node.nodeName === "SPAN") {
    if (node.classList.contains("filled")) {
      node.className = event.shiftKey ? "" : "empty";
    } else if (node.classList.contains("empty")) {
      node.className = event.shiftKey ? "filled" : "";
    } else {
      node.className = event.shiftKey ? "empty" : "filled";
    }

    const gridValues = localStorage.getItem("grid")
      ? JSON.parse(localStorage.getItem("grid"))
      : Array(grid.length);
    gridValues[node.dataset.i] = node.className;
    localStorage.setItem("grid", JSON.stringify(gridValues));
  }

  [].concat
    .apply([], node.parentNode.children)
    .map(checkCell)
    .forEach(function(good, i) {
      if (good) node.parentNode.children[i].classList.remove("error");
      else node.parentNode.children[i].classList.add("error");
    });
}

